        long to = pickerTo.getDate().getTime()/(24*60*60*1000);
        long today = System.currentTimeMillis()/(24*60*60*1000)-1;
        
        
        
        jLabelToFrom.setText(""+(to - from));
        jLabeltodayFrom.setText(""+(today - from));
        jLabelToToday.setText(""+(to - today));
